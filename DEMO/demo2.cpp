#include <iostream>
using namespace std;

int main()
{
    int no1,no2,ans;

    no1=20;
    no2=10;

    cout<<"\nno1 : "<<no1;
    cout<<"\nno2 : "<<no2;

    ans = no1+no2;
    cout<<"\naddition : "<<ans; 

    ans = no1-no2;
    cout<<"\nsubtraction : "<<ans; 

    ans = no1*no2;
    cout<<"\nmultiplication : "<<ans; 

    ans = no1/no2;
    cout<<"\ndivision : "<<ans; 

    return 0;
}
